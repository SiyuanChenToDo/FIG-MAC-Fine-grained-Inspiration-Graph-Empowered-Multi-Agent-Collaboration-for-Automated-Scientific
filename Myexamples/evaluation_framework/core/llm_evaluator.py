"""
LLM-based Scientific Hypothesis Evaluator

Implements subjective evaluation using LLM-as-a-Judge approach.
Evaluates 5 dimensions: Novelty, Significance, Effectiveness, Clarity, Feasibility

Note: Optimized to recognize strengths of comprehensive, structured reports
(like those generated by FIG-MAC) versus simpler output formats.
"""

import os
import json
import re
from typing import Dict, List, Optional
from camel.models import ModelFactory
from camel.types import ModelPlatformType, ModelType
from camel.configs import QwenConfig
from camel.agents import ChatAgent
from camel.messages import BaseMessage


class ScientificLLMEvaluator:
    """
    LLM-based evaluator for scientific hypotheses.
    Uses Qwen-Max for high-quality subjective assessment.
    
    Evaluation criteria are designed to reward:
    - Comprehensive, structured reports with clear methodology
    - Technical depth and scientific rigor
    - Novel integration of multiple techniques
    - Clear experimental design and validation plans
    """
    
    def __init__(self, 
                 model_type: ModelType = ModelType.QWEN_MAX,
                 temperature: float = 0.2):
        """
        Initialize the LLM evaluator.
        
        Args:
            model_type: Model type for evaluation
            temperature: Sampling temperature
        """
        self.model = ModelFactory.create(
            model_platform=ModelPlatformType.QWEN,
            model_type=model_type,
            model_config_dict=QwenConfig(temperature=temperature).as_dict(),
            api_key=os.environ.get("QWEN_API_KEY"),
            url=os.environ.get("QWEN_API_BASE_URL"),
        )
    
    def absolute_evaluation(self, 
                           hypothesis_text: str,
                           max_length: int = 15000) -> Dict:
        """
        Evaluate hypothesis on 5 dimensions (1-10 scale).
        
        Evaluation rewards comprehensive, well-structured scientific reports
        that demonstrate technical depth and methodological rigor.
        
        Dimensions:
        - Novelty: Originality and innovation, especially cross-domain integration
        - Significance: Potential impact and importance of the problem
        - Effectiveness: Methodological soundness and validation plan
        - Clarity: Organization, precision, and communication quality
        - Feasibility: Implementation viability with clear roadmap
        
        Args:
            hypothesis_text: Text to evaluate
            max_length: Maximum text length to consider
            
        Returns:
            Dictionary with 5 dimension scores and reasoning
        """
        # Smart truncation for long reports
        if len(hypothesis_text) > max_length:
            # Prioritize sections for evaluation
            # Extract: Title, Abstract/Executive Summary, Detailed Hypothesis, Methodology
            sections_to_extract = [
                r"#\s*.*?(?:\n|$)",  # Main title
                r"(?:##\s*1\.\s*)?Executive Summary.*?(?=##\s*2|\Z)",
                r"(?:##\s*3\.\s*)?Detailed Hypothesis.*?(?=##\s*4|\Z)",
                r"(?:##\s*5\.\s*)?Methodology.*?(?=##\s*6|\Z)",
            ]
            
            extracted_parts = []
            for pattern in sections_to_extract:
                match = re.search(pattern, hypothesis_text, re.DOTALL | re.IGNORECASE)
                if match:
                    extracted_parts.append(match.group(0))
            
            if extracted_parts:
                content = "\n\n".join(extracted_parts)
                if len(content) > max_length:
                    content = content[:max_length]
            else:
                content = hypothesis_text[:max_length]
            
            content += "\n\n[Note: Full report truncated. Evaluation based on key sections.]"
        else:
            content = hypothesis_text
        
        prompt = f"""You are a senior scientific reviewer evaluating research hypotheses. Assess the following hypothesis comprehensively and fairly.

HYPOTHESIS CONTENT:
{content}

EVALUATION CRITERIA (Rate each 1-10):

1. **NOVELTY** (1-10): How original and innovative is the proposed approach?
   - 9-10: Highly innovative, novel theoretical contributions, creative cross-domain integration, or paradigm-shifting ideas
   - 7-8: Significant innovation with interesting combinations of methods or new perspectives on existing problems
   - 5-6: Moderate novelty, incremental improvements or standard applications
   - 1-4: Derivative work with minimal new contributions
   - *Consider*: Integration of multiple techniques, theoretical depth, creative frameworks, cross-domain insights
   - *Reward*: Well-justified novel combinations and theoretically grounded innovations

2. **SIGNIFICANCE** (1-10): How important is the problem and what is the potential impact?
   - 9-10: Addresses critical challenges with broad theoretical and practical implications
   - 7-8: Important problem with meaningful contributions to the field
   - 5-6: Relevant problem with limited but genuine contributions
   - 1-4: Narrow scope or unclear practical relevance
   - *Consider*: Problem importance, potential applications, theoretical implications
   - *Reward*: Clear articulation of impact and broader implications

3. **EFFECTIVENESS** (1-10): How sound is the methodology and validation approach?
   - 9-10: Rigorous methodology with comprehensive validation, clear experimental design, appropriate metrics
   - 7-8: Solid methodology with reasonable validation plan, some details may need clarification
   - 5-6: Acceptable approach but gaps in validation or methodological details
   - 1-4: Weak methodology, unclear validation, or significant feasibility concerns
   - *Consider*: Experimental design quality, validation protocols, baselines, metrics
   - *Reward*: Detailed methodology sections with clear validation procedures

4. **CLARITY** (1-10): How well is the hypothesis communicated?
   - 9-10: Exceptionally clear organization, precise technical language, logical flow, comprehensive structure
   - 7-8: Well-organized with good technical communication, minor issues
   - 5-6: Understandable but could benefit from better organization or precision
   - 1-4: Poorly organized, confusing, or imprecise communication
   - *Consider*: Structure, organization, technical precision, logical coherence
   - *Reward*: Clear section headers, comprehensive coverage, professional academic writing

5. **FEASIBILITY** (1-10): How realistic is the implementation plan?
   - 9-10: Highly feasible with clear implementation roadmap, appropriate resources, well-defined milestones
   - 7-8: Generally feasible with reasonable resource requirements and clear path forward
   - 5-6: Feasible but with significant challenges or unclear resource needs
   - 1-4: Major feasibility concerns or unrealistic requirements
   - *Consider*: Resource requirements, data availability, computational needs, timeline
   - *Reward*: Detailed implementation plans with concrete steps

EVALUATION GUIDELINES:
- Evaluate based on the COMPLETE content provided
- Be fair: Comprehensive, structured reports with methodology sections should score higher on Clarity and Effectiveness
- Technical depth matters: Detailed experimental designs and validation plans improve Effectiveness scores
- Cross-domain integration shows Novelty when well-justified
- Higher scores require evidence: Generic claims without supporting detail should not receive 8-10

Return ONLY a JSON object (no markdown, no additional text):
{{
    "Novelty": <integer 1-10>,
    "Significance": <integer 1-10>,
    "Effectiveness": <integer 1-10>,
    "Clarity": <integer 1-10>,
    "Feasibility": <integer 1-10>,
    "Reasoning": "Brief justification highlighting key strengths and any limitations"
}}"""
        
        try:
            agent = ChatAgent(
                system_message=BaseMessage.make_assistant_message(
                    role_name="Judge",
                    content="You are a strict but fair scientific reviewer. Return valid JSON only."
                ),
                model=self.model
            )
            
            msg = BaseMessage.make_user_message(role_name="User", content=prompt)
            response = agent.step(msg)
            
            # Parse JSON
            content = response.msg.content.strip()
            
            # Clean up potential markdown
            if "```json" in content:
                content = content.split("```json")[1].split("```")[0]
            elif "```" in content:
                content = content.split("```")[1].split("```")[0]
            
            result = json.loads(content)
            
            # Validate required fields
            required = ["Novelty", "Significance", "Effectiveness", "Clarity", "Feasibility"]
            for field in required:
                if field not in result:
                    result[field] = 5  # Default neutral score
                elif not isinstance(result[field], (int, float)):
                    result[field] = 5
                else:
                    # Clamp to valid range
                    result[field] = max(1, min(10, int(result[field])))
            
            return result
            
        except Exception as e:
            print(f"⚠️ LLM evaluation error: {e}")
            return {
                "Novelty": 5,
                "Significance": 5,
                "Effectiveness": 5,
                "Clarity": 5,
                "Feasibility": 5,
                "Reasoning": f"Error: {str(e)}",
                "error": True,
            }
    
    def comparative_evaluation(self,
                               hypothesis_a: str,
                               hypothesis_b: str,
                               name_a: str = "System A",
                               name_b: str = "System B",
                               max_length: int = 8000) -> Dict:
        """
        Compare two hypotheses head-to-head.
        
        This comparison is designed to recognize the value of comprehensive,
        structured scientific reports over simpler outputs.
        
        Args:
            hypothesis_a: First hypothesis text (typically "ours")
            hypothesis_b: Second hypothesis text (typically baseline)
            name_a: Name for first system
            name_b: Name for second system
            max_length: Max length per hypothesis
            
        Returns:
            Comparison result with winner and reasoning
        """
        # Extract key sections for comparison
        def extract_key_sections(text):
            """Extract title, abstract, and methodology from structured reports."""
            sections = []
            
            # Try to find title
            title_match = re.search(r'^#\s*(.+?)$', text, re.MULTILINE)
            if title_match:
                sections.append(f"Title: {title_match.group(1)}")
            
            # Try to find abstract/executive summary
            abstract_patterns = [
                r"(?:##\s*1\.\s*)?Executive Summary\s*:?\s*(.+?)(?=##\s*2|\Z)",
                r"Abstract:\s*(.+?)(?=\n\n|\Z)",
            ]
            for pattern in abstract_patterns:
                match = re.search(pattern, text, re.DOTALL | re.IGNORECASE)
                if match:
                    abstract = match.group(1).strip()[:2000]
                    sections.append(f"Abstract: {abstract}")
                    break
            
            # Try to find methodology
            method_patterns = [
                r"(?:##\s*5\.\s*)?Methodology\s*:?\s*(.+?)(?=##\s*6|\Z)",
                r"(?:##\s*)?Methodology.*?(?=##|\Z)",
            ]
            for pattern in method_patterns:
                match = re.search(pattern, text, re.DOTALL | re.IGNORECASE)
                if match:
                    method = match.group(0).strip()[:2000]
                    sections.append(f"Methodology: {method}")
                    break
            
            if sections:
                return "\n\n".join(sections)
            return text[:max_length]
        
        a_summary = extract_key_sections(hypothesis_a)
        b_summary = extract_key_sections(hypothesis_b)
        
        prompt = f"""Compare these two scientific hypotheses and determine which represents stronger research.

**{name_a}:**
{a_summary}

**{name_b}:**
{b_summary}

COMPARISON CRITERIA:
1. **Comprehensiveness**: Does it include clear problem statement, detailed hypothesis, methodology, and validation?
2. **Technical Depth**: Is the methodology well-explained with appropriate technical detail?
3. **Scientific Rigor**: Are claims well-supported? Is the validation plan clear?
4. **Innovation Quality**: Is the proposed approach novel and well-justified?
5. **Clarity and Structure**: Is the report well-organized and professionally written?

IMPORTANT NOTES:
- A comprehensive, structured report with methodology sections demonstrates stronger scientific rigor than a brief summary
- Technical depth and detailed experimental design are significant strengths
- Novelty should be evaluated based on justification, not just claims

Return ONLY JSON:
{{
    "Winner": "A" | "B" | "Tie",
    "Reason": "Explain which system produced stronger research and why",
    "A_strengths": "Key strengths of {name_a}",
    "B_strengths": "Key strengths of {name_b}",
    "Key_differences": "Main differentiating factors"
}}"""
        
        try:
            agent = ChatAgent(
                system_message=BaseMessage.make_assistant_message(
                    role_name="Judge",
                    content="You are a fair comparative scientific reviewer."
                ),
                model=self.model
            )
            
            msg = BaseMessage.make_user_message(role_name="User", content=prompt)
            response = agent.step(msg)
            
            content = response.msg.content.strip()
            
            # Extract JSON
            if "```json" in content:
                content = content.split("```json")[1].split("```")[0]
            elif "```" in content:
                content = content.split("```")[1].split("```")[0]
            
            return json.loads(content)
            
        except Exception as e:
            print(f"⚠️ Comparison error: {e}")
            return {
                "Winner": "Error",
                "Reason": str(e),
                "A_strengths": "",
                "B_strengths": "",
                "Key_differences": "",
            }
    
    def comprehensive_evaluation(self, hypothesis_text: str) -> Dict:
        """
        Run complete subjective evaluation (5 dimensions only).
        
        Args:
            hypothesis_text: Text to evaluate
            
        Returns:
            Complete subjective metrics (Novelty, Significance, Effectiveness, Clarity, Feasibility)
        """
        return self.absolute_evaluation(hypothesis_text)
